package com.example.apptocomplete.myapplication58;

public class person0 {
    int Image ;
    String Name ;
    String Des ;
    String Id ;

    public person0(int image, String name, String des, String id) {
        Image = image;
        Name = name;
        Des = des;
        Id = id;
    }

    public int getImage() {
        return Image;
    }

    public void setImage(int image) {
        Image = image;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getDes() {
        return Des;
    }

    public void setDes(String des) {
        Des = des;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }
}
